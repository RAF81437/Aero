#include <stdio.h>

int main() {
    // Déclarer et initialiser deux nombres
    int nombre1 = 10;
    int nombre2 = 5;
    
    // Calculer la somme des deux nombres
    int somme = nombre1 + nombre2;
    
    // Afficher le résultat à l'écran
    printf("La somme de %d et %d est : %d\n", nombre1, nombre2, somme);

    return 0;
}
